                        <p>
                            В сухой градирне происходит разбрызгивание горечей воды, которая
                            смешивается с холодным воздухом. Часть воды, превращаясь в пар,
                            смешивается с наружным воздухом и уходит в атмосферу, другая же
                            часть охлаждается.<br />
                            Объект закрытого типа работает по принципу охлаждения воды
                            внутри замкнутого контура теплообменника, который обдувается
                            потоком наружного воздуха. Прямого контакта подаваемого воздуха
                            и охлаждающей жидкости не происходит.
                        </p>
                        <p>Мокрые градирни разделяют по способу передачи воздуха:</p>
                        <ul>
                            <li>поперечноточные</li>
                            <li>противоточные</li>
                            <li>брызгальные</li>
                            <li>эжекционные</li>
                        </ul>
                        <p>
                            По способу организации поверхности контакта мокрые градирни
                            делятся на:
                        </p>
                        <ul>
                            <li>оросительные (противоточные и поперечноточные)</li>
                            <li>безнасадочные (эжекторные и брызгальные)</li>
                        </ul>
                        <p>
                            В оросительных градирнях подаваемого воздуха контактирует с
                            охлаждаемой водой на развитой поверхности оросительного слоя.<br />
                            В безнасадочных градирнях вода разбивается на мелкие капли и
                            процесс теплообмена происходит на каждой капле.
                        </p>
                        <p>
                            Среди противоточных градирен выделяют башенные и вентиляторные.
                            Противоточные градирни состоят из резервуара для сбора остывшей
                            воды, сетчатого оросителя, над оросителем находится система
                            подачи горячей воды с соплами. Через сопла горячая вода
                            поступает на ороситель, смешивается с наружным воздухом,
                            остывает и попадает в резервуар. Наружный воздух подается через
                            ороситель за счет естественной конвекции воздуха или путём
                            нагнетания его вентилятором. Если подача идет в естественном
                            режиме – это башенный объект, если нагнетается, то
                            вентиляторный.
                        </p>
