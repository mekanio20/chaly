                <h1 class="global-title">Очистка градирен</h1>
                <div class="news-contant">
                    <div class="text-wrap">
                        <p>
                            Градирня – это инженерное сооружение, предназначенное для
                            охлаждения большого количества воды. Широкое применение градирни
                            нашли в системах оборотного водоснабжения. Данное устройство
                            предохраняет технологическое оборудование от нагрева и
                            разрушения под действием высоких температур. Водооборотные
                            системы часто используются в металлургии, энергетике, в
                            машиностроительной, судостроительной и прочих отраслях.<br />
                            В зависимости от специфики производств, были разработаны два
                            типа градирен – сухие и мокрые. В сухих используют закрытый
                            контур с охлаждающей жидкостью, в мокрых – открытый.
                        </p>
                        <h2>Виды градирен</h2>
                        <p>По типу контура:</p>
                        <ul>
                            <li>закрытые (сухие)</li>
                            <li>открытые (мокрые)</li>
                        </ul>
                        <p>
                            В сухих градирнях теплопередающей поверхностью служат радиаторы,
                            для отвода нагретого воздуха они оборудуются вентиляторами.
                        </p>
