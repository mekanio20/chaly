                            <span style="color: #999999">Тип &#8212; башенный</span>
                        </h5>
                        <p>
                            Башенная имеет вид конической трубы из железобетона или металл.
                            Внутри имеет систему подачи воды, ороситель и резервуар. Поток
                            наружного воздуха через входные отверстия в нижней части трубы
                            поднимается вверх через ороситель за счет создания естественной
                            тяги в трубе.<br />
                            Данные градирни получили большое распространение в крупной
