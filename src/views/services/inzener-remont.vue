                                далее для точного подбора реагента необходимо выполнить отбор
                                проб отложений;
                            </li>
                            <li>непосредственно сама промывка;</li>
                            <li>
                                обработка сети труб ингибиторами для защиты от коррозии;
                            </li>
                            <li>опрессовка системы.</li>
