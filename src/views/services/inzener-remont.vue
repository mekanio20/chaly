                        <h2>Гидропневматическая промывка инженерных систем</h2>
                        <p>
                            Гидропневматическая промывка отопительной системы проводится
                            напором сжатого воздуха. В сеть подается под давлением сжатый
                            воздух, при этом скорость движения жидкости в системе резко
                            увеличивается, а неравномерность движения сжатого воздуха
                            обеспечивает механический отрыв отложений с поверхности труб.
                            Отложения выносятся потоком воды наружу через точку сброса.<br />
                            Для гидропневматической промывки специалисты компании
                            «Chaly» trading service используют компрессор, который может добавлять в
                            поток воздуха реагенты для очищения и дезинфекции, а так же
                            который на выходе выдаёт давление не меньше 0,6 МПа.
