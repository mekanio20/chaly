                        </p>
                        <p style="text-align: justify">
                            Есть определенные доказанные цифры по экономии ресурсов, а
                            именно: 1 мм сажи или отложений на теплопередающих поверхностях
                            снижает КПД агрегата на 5-7% &#8212; другими словами, если при
                            чистом котле вы платили за газ в месяц 100 рублей, то при
                            грязном (условно) будете платить 107 рублей, а при очень грязном
                            121 рубль. После окончательного загрязнения котел просто порвет
                            деформация металла или тепловые расширения. А если еще прибавить
                            эффект от отложений внутри котла (водяная или паровая сторона),
                            то умножаем на два. Вот по этому очень важно своевременно
                            проводить техническое обслуживание оборудования и агрегатов
                            задействованных в работе вашего инженерного комплекса.
                        </p>
                        <h4 style="text-align: justify">
                            Описание процесса очистки топочного пространства котла
                        </h4>
                        <p style="text-align: justify">
                            Промывка котла по газовой или дизельной стороне.<br />
                            Каждая операция по чистке или обслуживанию котла осуществляется
                            после отключения топливного и электрического питания.
                        </p>
                        <p style="text-align: justify">
                            Чистка поверхностей нагрева должна производиться:
                        </p>
                        <ul>
                            <li style="text-align: justify">
                                один раз в три месяца при работе на дизельном топливе,
                            </li>
                            <li style="text-align: justify">
                                один раз в год при работе на природном газе.
                            </li>
                        </ul>
                        <p style="text-align: justify">
                            При использовании согласованных других видов топлива
                            периодичность очистки определяется по результатам осмотров
                            теплообменных поверхностей и замеров температур уходящих газов.
                            Периодичность чистки зависит также от правильности настройки
                            режима горения.
                        </p>
                        <p style="text-align: justify">
                            При повышении конечной температуры уходящих газов на 30 °С по
                            сравнению с температурой при чистом котле на заданной нагрузке,
                            необходимо провести чистку:
                        </p>
                        <ul>
                            <li style="text-align: justify">
                                открыть фронт, удалить интенсификаторы;
                            </li>
                            <li style="text-align: justify">
                                выполнить механическую очистку жаровой трубы скребками и
                                щетками, а дымогарных труб – ершами. Из труб второго хода сажа
                                удаляется в коллектор дымовых газов. Сажу необходимо удалять
                                из топки и коллектора дымовых газов пылесосом или ручным
                                способом после каждой чистки;
                            </li>
                            <li style="text-align: justify">
                                установить интенсификаторы, поврежденные заменить;
                            </li>
                            <li style="text-align: justify">
                                очистить поверхность огнеупорного слоя фронтальной дверцы
                                котла мягкой неметаллической щеткой-сметкой. Убедиться в
                                отсутствии повреждений теплоизоляции (футеровки). В случае
                                необходимости произвести ремонт теплоизоляции.
                            </li>
                        </ul>
                        <p style="text-align: justify">
                            Примерно такие же операции проделывают при чистке малых
                            настенных котлов и больших паровых котлов с множеством трубных
                            (конвективных) пучков внутри топки.
                        </p>
