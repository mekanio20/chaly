                <h1 class="global-title">
                    Замена теплоносителя в инженерных системах зданий
                </h1>
                <div class="news-contant">
                    <div class="text-wrap">
                        <p>
                            Для нашей страны характерно в качестве теплоносителя для
                            отопительных систем использовать воду, связано это со следующими
                            факторами:
                        </p>
                        <ol>
                            <li>Вода является лучшим переносчиком тепла.</li>
                            <li>
                                Вода не токсична, ее пары всегда содержатся в воздухе, ее
                                можно неограниченно сливать как на грунт, так и в сточные
                                системы.
                            </li>
                            <li>Не требует специальной подготовки перед утилизацией.</li>
                            <li>Низкая стоимость, всегда под рукой.</li>
                        </ol>
                        <p>
                            Но не смотря на все свои плюсы, имеется ряд отрицательных
                            качеств, которые не позволяют использовать ее в качестве
                            теплоносителя повсеместно. Это высокая температура замерзания −
                            0 °C.
                        </p>
                        <p>
                            При замерзании вода расширяется, что приводит к выводу системы
                            отопления из строя, если же воду из системы слить, то процесс
                            коррозии внутри нее пойдет еще быстрее, что в свою очередь не
                            продлит срок эксплуатации системы отопления.
                        </p>
                        <h2>
                            Специалисты компании «Chaly» trading service производят замену воды в
                            системах отопления следующим образом:
                        </h2>
                        <p>
                            Перед заливом специалисты компании «Chaly» trading service проверят
                            систему отопления на неисправности. Так же необходимо рассчитать
                            количество теплоносителя в системе. Существуют специальные
                            таблицы, позволяющие без труда это сделать. Расчет производится
                            на 1 погонный метр трубы. Пользуясь данными таблицами и зная
                            диаметр труб, можно вычислить, какое количество теплоносителя
                            потребуется на 10 метров системы.
                        </p>
                        <p>Далее начинается сам процесс залива теплоносителя.</p>
                        <p>
                            Первая стадия − это слив из системы предыдущего
                            теплоносителя.<br />
                            Система отопления отсекается и отключается, температура воды в
                            системе должна достигнуть комнатной. Далее наши специалисты
                            сливают остывшую воду из системы через нижнюю точку. После того,
                            как вся вода слилась, проводят стабилизацию давления в системе
                            путем открытия крана в верхней точке.
                        </p>
                        <p>
                            Затем происходит промывка системы – с помощью насоса в трубы
                            закачивается промывочный реагент для полного удаления отложений
                            из системы, затем он вымывается вместе с отложениями. Цикл
                            повторяют несколько раз.
                        </p>
                        <h3>Закачка в систему теплоносителя</h3>
                        <p>
                            С помощью насоса теплоноситель закачивается в систему до тех
                            пор, пока не потечет из верхней точки − это знак о том, что
                            система заполнена. Далее специалисты компании «Chaly» trading service
                            проверяют давление в системе, проверяют исправность всех
                            измерительных приборов и для проверки герметичности системы
                            проводят ее опрессовку.
                        </p>
                        <p>
                            Заполнение системы этиленгликолем и пропиленгликолем происходит
                            аналогичным образом.
                        </p>
                        <p>
                            Сейчас теплотехники и собственники автономных систем все чаще
                            используют в качестве теплоносителей водные растворы на основе
                            этилен и пропиленгликоля − они так же доступны по цене, а
                            температура их замерзания ниже, чем у воды, благодаря добавлению
                            в них присадок и ингибиторов, они не дают вспенивания и защищают
                            систему от коррозии. Процентное содержание основного компонента
                            в растворе определяет тепло-физические характеристики
                            теплоносителя, а именно — теплопроводность, теплоемкость,
                            вязкость, диапазон допустимых рабочих температур жидкости,
                            обеспечивающие безопасный режим эксплуатации отопительного или
                            охлаждающего оборудования.
                        </p>
