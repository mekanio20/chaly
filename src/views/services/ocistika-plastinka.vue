                        <p>
                            Разборный пластинчатый теплообменник включает в себя раму и
                            пакет пластин.
                        </p>
                        <p>
                            Рама состоит из неподвижной плиты (1) и прижимной плиты (2),
                            задней стойки (7) которая соединена с неподвижной плитой верхней
                            направляющей (3) и нижней направляющей (4). Рамы разборных
                            теплообменников выпускаются разной длины для обеспечения
                            установки в нее разного количества пластин. Между двумя рамами
                            находится пакет определённого количества пластин.
                        </p>
                        <p>
                            Пакет прижат к неподвижной плите прижимной плитой с помощью
                            резьбовых стяжек (6). При определённом сжатии уплотнения
                            контактируют между собой и герметизируют внутреннее
                            пространство.
                        </p>
                        <p>
                            Пластины изготовляются методом штамповки из нержавеющей стали.
                            Марки стали, используемые для изготовления пластин не склонны к
                            общим видам коррозии. Пластины имеют четыре проходных отверстия,
                            которые образуют две системы каналов и изолированы друг от
                            друга. Резиновые прокладки служат для уплотнения пластин и
                            каналов при сжатии. Уплотнения крепятся на пластину с помощью
                            клипс, при этом у каждой марки теплообменников свои крепления.
                        </p>
