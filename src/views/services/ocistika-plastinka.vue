                            Во время эксплуатации теплообменного оборудования происходит
                            износ его частей, а показатели работы ухудшаются. Это связано с
                            тем, что в рабочие части теплообменника попадают крупные частицы
                            отложений, а из-за плохого качества подготовки воды на
                            внутренних поверхностях оборудования образуются отложения солей
                            кальция, магния, продуктов жизнедеятельности микроорганизмов.
                            Если вы заметили, что показатели работы вашего теплообменного
                            оборудования снизились, скорее всего требуется его обслуживание
                            и очистка. Как правило, чистку теплообменного оборудования
                            проводят перед каждым отопительным сезоном, это позволяет
                            избегать крупных аварий и поломки оборудования, что ведёт к
                            дорогостоящим и долговременным работам. Компания «Chaly-trading-Сервис»
                            осуществляет очистку теплообменников разборным и безразборным
                            способами. Оба способа имеют свои положительные стороны.
                        </p>
                        <h2>Разборная промывка теплообменников</h2>
                        <p>
                            Проводятся процедуры по разборке теплообменника, для начала он
                            отключается от общей системы, воду из него сливают и ослабляют
                            шпильки, сжимающие пакет пластин. Стоит отметить, что необходимо
                            строго соблюдать методику разборки и сборки теплообменника, в
                            противном случае можно его «перетянуть», пережать уплотнения
                            пластин, что приведёт к очередной разборке оборудования и несёт
                            дополнительные расходы, а иногда может привести к серьёзному
                            ремонту. Именно поэтому мы советуем доверить очистку и
                            обслуживание ваших аппаратов нашим специалистам, имеющим большой
                            опыт в данных работах. После разборки пакет пластин снимается и
                            каждая пластина в отдельности промывается при помощи аппарата
                            высокого давления, при необходимости, предварительно перед
                            обработкой АВД пластины замачиваются в химическом растворе для
                            удаления сцепки отложений с поверхностью металла пластины.
                        </p>
                        <p>
                            Разборная промывка, как правило, применяется, когда
                            теплообменник почти полностью забит и пропускная способность его
                            крайне низка. Затем теплообменник собирается, здесь важно
                            правильно собрать пакет пластин и уплотнений. В отдельных
                            случаях, когда теплообменнику более 8 лет и уплотнения ни разу
                            не менялись, при разборке/сборке необходимо поменять весь
                            комплект уплотнений.
