                        <h2>Полусварной пластинчатый теплообменник</h2>
                        <p>
                            Состоит из набора сварных модулей, образованный из двух пластин,
                            которые сварены между собой. Резиновые прокладки между каналами
                            аналогичны пластинчатым теплообменникам. Полусварной
                            пластинчатый идеально подходит для агрессивных сред. Агрессивное
                            вещество протекает по каналам, образованным сваренными между
                            собой пластинами.
